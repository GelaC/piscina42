int		ft_str_is_lowercase(char *str)
{
	int i;
	int r;

	i = 0;
	r = 1;
	while (str[i] != '\0')
	{
		if (str[i] > 'z' || str[i] < 'a')
			r = 0;
		i++;
	}
	return (r);
}
