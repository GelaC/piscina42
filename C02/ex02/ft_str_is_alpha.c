int	ft_str_is_alpha(char *str)
{
	int i;
	int r;

	i = 0;
	r = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			if (!(str[i] >= 'A' && str[i] <= 'Z'))
				r = 0;
		i++;
	}
	return (r);
}
