int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;
	unsigned int	j;
	int				f;

	i = 0;
	j = 0;
	f = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[j] != '\0')
	{
		j++;
	}
	f = i - j;
	return (f);
}
